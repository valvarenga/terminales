<h2 class="text-success fw-bolder">¿Porqué existe este proyecto</h2>
<p class="fst-italic fs-3">
A lo largo de los años, los Estelianos siempre hemos tenido la dificultad de conocer con exactitud la hora de salida de los buses de la terminal norte y de la terminal sur. La única forma es llamando por teléfono directamente a las terminales, pero también algunas personas no tienen el acceso a conocer el número de teléfono de cada terminal, inclusive tener que ir personalmente a las terminales para solicitar dicha información.

Así que decidí crear este proyecto, con la idea principal para intentar de solventar esa problemática o dificultad que existe en relación a los horarios de las terminales de buses, así, todas las personas que recidan en Estelí o cualquier turista que desee viajar desde Estelí hacia el interior del país, puedan conocer fácilmente los horarios y todos los destinos de dichas terminales y esté accesible para todos.

Ya que de ésta forma cualquier información que la población de Estelí necesite estará a su alcance y hará mucho más fácil, conocer la información relacionada a cada terminal de buses.

Es por eso que es importante, que al conocer este proyecto lo compartas con todos tus familiares, amigos y conocidos, para así ayudar a más personas.
